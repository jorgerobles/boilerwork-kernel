     * Review the utility of this method
     */
    public static function fromIso6391Code(Iso6391Code $code): ?string
    {
        foreach (static::$data as $lang => $name) {
            if ($code->equals(new Iso6391Code($lang))) {
                return $lang;
            }
        }
        return null;
    }

